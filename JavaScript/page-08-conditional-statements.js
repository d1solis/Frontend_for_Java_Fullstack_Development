// If-else statement
let age = 10;

if (age >= 18){
    console.log("You are an adult.");
}else{
    console.log("You are not yet an adult.");
    // Expected output: You are not yet an adult.
}

// Switch statement
let day = 4;

switch(day){
    case 1:
        console.log("Monday");
        break;
    case 2:
        console.log("Tuesday");
        break;
    default:
        console.log('Invalid Day');
        break;
        // Expected output: Invalid Day
}