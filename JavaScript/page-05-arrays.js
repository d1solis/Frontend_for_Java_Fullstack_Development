// fruits array

let fruits = ['apples', 'oranges', 'bananas'];      // Array of strings
console.log(fruits[0]);
// Expected output: apples

fruits.push('grapes');      // Adding an element to an end of the array
console.log(fruits);
// Expected output: ['apples', 'oranges', 'bananas', 'grapes']

fruits.pop();       // Removing an element from the end of the array
console.log(fruits);
// Expected output: ['apples', 'oranges', 'bananas']

