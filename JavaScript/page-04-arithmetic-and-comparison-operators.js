// These are all arithmetic operators:
let x = 10;
let y = 5;

let sum = x + y;            // Addition
console.log(sum);
// Expected output: 15

let difference = x - y;     // Subtraction
console.log(difference);
// Expected output: 5

let product = x * y;        // Multiplication
console.log(product);
// Expected output: 50

let quotient = x / y;       // Division
console.log(quotient);
// Expected output: 2

let remainder = x % y;      // Modulus
console.log(remainder);
// Expected output: 0

let result = x ** y;        // Exponential 
console.log(result);
// Expected output: 100,000

console.log('**************************************');

// These are all comparison operators:
let a = 10;
let b = 5;

console.log(a == b);        // Equal to
console.log(a != b);        // Not equal to
// Expected output: False
// Expected output: True

console.log(a === b);       // Strict equal to
console.log(a !== b);       // Strict equal not to
// Expected output: False
// Expected output: True

console.log(a > b);         // Greater than
console.log(a >= b);        // Greater than or equal to
// Expected output: True
// Expected output: True

console.log(a < b);         // Less than
console.log(a <= b);        // Less than or equal to
// Expected output: False
// Expected output: False